<template>
    <div class="mt-6 -mb-1 flex flex-wrap">
        <template v-for="(link, key) in links">
            <div
                v-if="link.url === null"
                :key="key"
                class="button button-soft mr-1 mb-1"
                :class="{ 'ml-auto': link.label === 'Next' }"
            >
                {{ link.label }}
            </div>

            <inertia-link
                v-else
                :key="key"
                class="button mr-1 mb-1"
                :class="{ 'bg-white': link.active, 'ml-auto': link.label === 'Next' }"
                :href="link.url"
            >
                {{ link.label }}
            </inertia-link>
        </template>
    </div>
</template>

<script>
export default {
    name: 'Pagination',

    props: {
        links: {
            type: Array,
            required: true
        }
    }
};
</script>
