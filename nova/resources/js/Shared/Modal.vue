<template>
    <portal to="modals">
        <div
            v-if="showModal"
            class="fixed inset-0 flex justify-center pt-16"
            @click="close"
        >
            <transition
                enter-class="opacity-0"
                enter-active-class="ease-out"
                enter-to-class="opacity-100"
                leave-class="opacity-100"
                leave-active-class="ease-in"
                leave-to-class="opacity-0"
                appear
                @before-leave="backdropLeaving = true"
                @after-leave="backdropLeaving = false"
            >
                <div v-if="showBackdrop">
                    <div class="absolute inset-0 bg-black opacity-50 transition-color transition-fastest"></div>
                </div>
            </transition>

            <transition
                enter-class="opacity-0 scale-125"
                enter-active-class="ease-out"
                enter-to-class="opacity-100 scale-100"
                leave-class="opacity-100 scale-100"
                leave-active-class="ease-in"
                leave-to-class="opacity-0 scale-125"
                appear
                @before-leave="cardLeaving = true"
                @after-leave="cardLeaving = false"
            >
                <div v-if="showContent" class="relative transition-all transition-fast transform-origin-c">
                    <div class="modal">
                        <div class="modal-title">{{ title }}</div>

                        <div class="modal-body">
                            <slot></slot>
                        </div>

                        <div class="modal-footer">
                            <slot name="footer"></slot>
                        </div>
                    </div>
                </div>
            </transition>
        </div>
    </portal>
</template>

<script>
export default {
    props: {
        open: {
            type: Boolean,
            default: false
        },
        title: {
            type: String,
            required: true
        }
    },

    data () {
        return {
            showModal: false,
            showBackdrop: false,
            showContent: false,
            backdropLeaving: false,
            cardLeaving: false
        };
    },

    computed: {
        leaving () {
            return this.backdropLeaving || this.cardLeaving;
        }
    },

    watch: {
        open: {
            handler (newValue) {
                if (newValue) {
                    this.show();
                } else {
                    this.close();
                }
            },
            immediate: true
        },

        leaving (newValue) {
            if (newValue === false) {
                this.showModal = false;
                this.$emit('close');
            }
        }
    },

    created () {
        const onEscape = (e) => {
            if (this.open && e.keyCode === 27) {
                this.close();
            }
        };

        document.addEventListener('keydown', onEscape);

        this.$once('hook:destroyed', () => {
            document.removeEventListener('keydown', onEscape);
        });
    },

    methods: {
        show () {
            this.showModal = true;
            this.showBackdrop = true;
            this.showContent = true;
        },

        close () {
            this.showBackdrop = false;
            this.showContent = false;
        }
    }
};
</script>
