        <h2>Post types</h2>

        <p>Post types is a new way to control the different categories of posts you can compose for your stories. This means there's no longer hard-coded story posts and personal logs, but instead, you can create whatever category of posts that you want to suit your game.</p>

        <p>Post types allow an incredible amount of control and customizability to your game. You can specify which fields are shown for a particular post type, if each field that's shown requires a value to submit or not, and even options to control whether posts of a particular kind are included in activity tracking.</p>

        <h2>Non-linear storytelling</h2>

        <p>Since its inception, Nova (and the SIMM Management System before it) only allowed telling stories in a linear fashion. When you finished with your current story, the next story you wanted to tell came after the previous one. Some people managed to come up with creative solutions to get around this, but now Nova includes the ability to tell stories in whatever order you want.</p>
