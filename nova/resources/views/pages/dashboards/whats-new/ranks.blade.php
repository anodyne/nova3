    <x-h2>Multi-image rank composition</x-h2>

    <p>
        Until now, rank images have always been a single, static image. This required a lot of work on the part of
        graphic designers to design all of the possible permutations. It also required storing each of the images
        individually and then referencing them in a painstaking process.
    </p>

    <p>
        Today, ranks are made up of 2 distinct images: a base image and an overlay image. This significantly reduces the
        number of files needed, allows graphic designers to work more quickly, and provides for thousands of
        combinations when composing a new rank. Simply select a base image and an overlay image and you're done.
    </p>

